    private void jBtn_RechercherEmplActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_RechercherEmplActionPerformed
        String recherche = jTextF_Rechercher.getText().trim();

    if (recherche.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez entrer un nom ou un ID pour rechercher.", "Erreur", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        List<Employe> resultats = Employe.rechercherEmployeParNomOuId(recherche);

        if (resultats.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ùå Aucun employ√© trouv√©.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Si plusieurs r√©sultats, proposer une s√©lection avec JComboBox
        Employe employeSelectionne;
        if (resultats.size() > 1) {
            Employe[] employesArray = resultats.toArray(new Employe[0]);
            Employe choix = (Employe) JOptionPane.showInputDialog(
                this,
                "Plusieurs employ√©s trouv√©s. S√©lectionnez celui que vous cherchez :",
                "S√©lection d'un employ√©",
                JOptionPane.QUESTION_MESSAGE,
                null,
                employesArray,
                employesArray[0]
            );

            if (choix == null) {
                return; // L'utilisateur a annul√©
            }
            employeSelectionne = choix;
        } else {
            employeSelectionne = resultats.get(0); // Un seul r√©sultat, on l'affiche directement
        }

        // R√©cup√©rer l'adresse de l'employ√© s√©lectionn√©
        Adresse adr = Adresse.getAdresseById(employeSelectionne.getIdAdresse());

        // Construire le message d'affichage
        String message = "üìå **Informations de l'Employ√©** :\n"
                + "üÜî ID : " + employeSelectionne.getIdEmploye() + "\n"
                + "üë§ Nom : " + employeSelectionne.getNom() + " " + employeSelectionne.getPrenom() + "\n"
                + "üè¢ Titre : " + employeSelectionne.getTitre() + "\n"
                + "üí∞ Salaire : " + employeSelectionne.getSalaire() + " ‚Ç¨\n"
                + "üìå Adresse : " + (adr != null ? adr.getNomRue() + ", " + adr.getLocalite() + ", " + adr.getPays() : "Non renseign√©e") + "\n"
                + "üîë ID User : " + employeSelectionne.getIdUser() + "\n";

        JOptionPane.showMessageDialog(this, message, "Employ√© trouv√©", JOptionPane.INFORMATION_MESSAGE);

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "‚ùå Erreur lors de la recherche.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBtn_RechercherEmplActionPerformed

